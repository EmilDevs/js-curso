// Stings
let nombre = 'Peter Parker';
console.log(nombre);

nombre = 'Ben Parker';
console.log(nombre);

nombre = "Tía May";
nombre = `Tía May`;

console.log(typeof nombre);

// Reasignación de tipo de dato
nombre = 123;
console.log(typeof nombre);

// Booleanos
let esMarvel = false;
console.log(typeof esMarvel);

// Numeros
let edad = 27;
console.log(typeof edad);

edad = 27.001;
console.log(typeof edad);

// Undefined variable sin inicializar valor
let superPoder;
console.log(superPoder);

// Null
let soyNull = null;
console.log(typeof soyNull);

// Symbol
let symbol1 = Symbol('a');
let symbol2 = Symbol('a');

console.log(typeof symbol1);

console.log(symbol1 === symbol2);